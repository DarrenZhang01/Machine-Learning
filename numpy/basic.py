import numpy as np
# 3 x 1 array
a = np.array([1, 2, 3])
print("Print out array a:")
print(a)
print("The size of the array a:")
print(a.shape)  # output: (3,) representing it is a 3 x 1 array
# 2 x 2 array
b = np.array([[1, 2], [3, 4]])
print("Print out array b:")
print(b)
print("The size of the array b:")
print(b.shape)   # output: (2, 2) representing it is a 2 x 2 array
